/** 予想学習日数からPhase閾値を生成（既存マスターの比率に合わせた） */
export function derivePhaseThresholds(estimatedDays: number): PhaseThresholds {
  const d = Math.max(1, estimatedDays);
  return {
    germinationInput: Math.round(d * 6),
    colonizationInput: Math.round(d * 24),
    primordiaOutput: Math.round(d * 9.6),
    fruitingTrigger: Math.round(d * 3.6),
    maturationTotal: Math.round(d * 60),
    awakeningTotal: Math.round(d * 75),
    transformationTotal: Math.round(d * 96),
    legendaryTotal: Math.round(d * 120),
  };
}

/** カスタム資格メタを生成 */
export function buildCustomCertMeta(params: {
  name: string;
  description?: string;
  tips?: string;
  templateId: string;
  difficulty: 1 | 2 | 3 | 4 | 5;
  estimatedDays: number;
}): CustomCertificationMeta {
  const tpl = CUSTOM_CERT_TEMPLATES.find((t) => t.id === params.templateId)
    ?? CUSTOM_CERT_TEMPLATES[0];
  return {
    name: params.name.trim() || "自由目標",
    description: params.description?.trim() || "ユーザーが自由に設定した学習目標",
    tips: params.tips?.trim() || "毎日少しずつ、自分のペースで進めましょう。",
    mushroomSpeciesId: tpl.mushroomSpeciesId,
    difficulty: params.difficulty,
    domains: [...tpl.domains] as DomainValues,
    estimatedDays: Math.max(1, params.estimatedDays),
    phaseThresholds: derivePhaseThresholds(params.estimatedDays),
  };
}

