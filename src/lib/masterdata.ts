/**
 * 栽培から資格情報を解決する（カスタム資格ならcustomCertを返す）。
 * 画面側でのnullチェックをこれに寄せると表示まわりがシンプルになる。
 */
export function resolveCultivationCert(
  cultivation: Pick<Cultivation, "certificationId" | "customCert">,
): CertificationMaster | CustomCertificationMeta & { id: string; category: string; prerequisiteId: string | null; lineageGroup: string } | undefined {
  if (cultivation.certificationId === "custom" && cultivation.customCert) {
    return {
      id: "custom",
      category: "Custom",
      prerequisiteId: null,
      lineageGroup: "custom",
      ...cultivation.customCert,
    };
  }
  return getCertificationById(cultivation.certificationId);
}

